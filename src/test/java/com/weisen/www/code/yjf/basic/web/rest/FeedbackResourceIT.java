    @Test
    @Transactional
    public void createFeedback() throws Exception {
        int databaseSizeBeforeCreate = feedbackRepository.findAll().size();

        // Create the Feedback
        FeedbackDTO feedbackDTO = feedbackMapper.toDto(feedback);
        restFeedbackMockMvc.perform(post("/api/feedbacks")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(feedbackDTO)))
            .andExpect(status().isCreated());

        // Validate the Feedback in the database
        List<Feedback> feedbackList = feedbackRepository.findAll();
        assertThat(feedbackList).hasSize(databaseSizeBeforeCreate + 1);
        Feedback testFeedback = feedbackList.get(feedbackList.size() - 1);
        assertThat(testFeedback.getName()).isEqualTo(DEFAULT_NAME);
        assertThat(testFeedback.getFeedbackdate()).isEqualTo(DEFAULT_FEEDBACKDATE);
        assertThat(testFeedback.getTitle()).isEqualTo(DEFAULT_TITLE);
        assertThat(testFeedback.getContent()).isEqualTo(DEFAULT_CONTENT);
        assertThat(testFeedback.getState()).isEqualTo(DEFAULT_STATE);
        assertThat(testFeedback.getImageurl()).isEqualTo(DEFAULT_IMAGEURL);
        assertThat(testFeedback.getCreator()).isEqualTo(DEFAULT_CREATOR);
        assertThat(testFeedback.getCreatedate()).isEqualTo(DEFAULT_CREATEDATE);
        assertThat(testFeedback.getModifier()).isEqualTo(DEFAULT_MODIFIER);
        assertThat(testFeedback.getModifierdate()).isEqualTo(DEFAULT_MODIFIERDATE);
        assertThat(testFeedback.getModifiernum()).isEqualTo(DEFAULT_MODIFIERNUM);
        assertThat(testFeedback.isLogicdelete()).isEqualTo(DEFAULT_LOGICDELETE);
        assertThat(testFeedback.getOther()).isEqualTo(DEFAULT_OTHER);
    }

