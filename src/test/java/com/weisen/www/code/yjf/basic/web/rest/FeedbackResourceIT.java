    @Test
    @Transactional
    public void updateFeedback() throws Exception {
        // Initialize the database
        feedbackRepository.saveAndFlush(feedback);

        int databaseSizeBeforeUpdate = feedbackRepository.findAll().size();

        // Update the feedback
        Feedback updatedFeedback = feedbackRepository.findById(feedback.getId()).get();
        // Disconnect from session so that the updates on updatedFeedback are not directly saved in db
        em.detach(updatedFeedback);
        updatedFeedback
            .name(UPDATED_NAME)
            .feedbackdate(UPDATED_FEEDBACKDATE)
            .title(UPDATED_TITLE)
            .content(UPDATED_CONTENT)
            .state(UPDATED_STATE)
            .imageurl(UPDATED_IMAGEURL)
            .creator(UPDATED_CREATOR)
            .createdate(UPDATED_CREATEDATE)
            .modifier(UPDATED_MODIFIER)
            .modifierdate(UPDATED_MODIFIERDATE)
            .modifiernum(UPDATED_MODIFIERNUM)
            .logicdelete(UPDATED_LOGICDELETE)
            .other(UPDATED_OTHER);
        FeedbackDTO feedbackDTO = feedbackMapper.toDto(updatedFeedback);

        restFeedbackMockMvc.perform(put("/api/feedbacks")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(feedbackDTO)))
            .andExpect(status().isOk());

        // Validate the Feedback in the database
        List<Feedback> feedbackList = feedbackRepository.findAll();
        assertThat(feedbackList).hasSize(databaseSizeBeforeUpdate);
        Feedback testFeedback = feedbackList.get(feedbackList.size() - 1);
        assertThat(testFeedback.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testFeedback.getFeedbackdate()).isEqualTo(UPDATED_FEEDBACKDATE);
        assertThat(testFeedback.getTitle()).isEqualTo(UPDATED_TITLE);
        assertThat(testFeedback.getContent()).isEqualTo(UPDATED_CONTENT);
        assertThat(testFeedback.getState()).isEqualTo(UPDATED_STATE);
        assertThat(testFeedback.getImageurl()).isEqualTo(UPDATED_IMAGEURL);
        assertThat(testFeedback.getCreator()).isEqualTo(UPDATED_CREATOR);
        assertThat(testFeedback.getCreatedate()).isEqualTo(UPDATED_CREATEDATE);
        assertThat(testFeedback.getModifier()).isEqualTo(UPDATED_MODIFIER);
        assertThat(testFeedback.getModifierdate()).isEqualTo(UPDATED_MODIFIERDATE);
        assertThat(testFeedback.getModifiernum()).isEqualTo(UPDATED_MODIFIERNUM);
        assertThat(testFeedback.isLogicdelete()).isEqualTo(UPDATED_LOGICDELETE);
        assertThat(testFeedback.getOther()).isEqualTo(UPDATED_OTHER);
    }

